package com.xworkz.ControlStatements;

public class ClassFees {

	public static void main(String[] args) {
    int Standard=7;
    if(Standard==1) {
    	System.out.println("Fee for Ist Standard is 100000");
    }else if(Standard==2){
    	System.out.println("Fee for 2nd Standard is 200000");
    }else if (Standard==3) {
    	System.out.println("Fee for 3rd Standard is 300000");
    }else if(Standard==4) {
    	System.out.println("Fee for 4th Standard is 400000");
	}else if (Standard==5) {
    	System.out.println("Fee for 5th Standard is 500000");
	}else {
    	System.out.println("Not Specified");

	}
	}
}

