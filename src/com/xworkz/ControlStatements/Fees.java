package com.xworkz.ControlStatements;

public class Fees {

	public static void main(String[] args) {
    int fee =6;
    switch(fee) {
    case 1: System.out.println("Fee for 1st class is 1L");
    break;
    case 2: System.out.println("Fee for 2nd class is 2L");
    break;
    case 3: System.out.println("Fee for 3rd class is 3L");
    break;
    case 4: System.out.println("Fee for 4th class is 4L");
    break;
    case 5: System.out.println("Fee for 5th class is 5L");
    break;
    default: System.out.println("Invalid Class");
    }
	}

}
