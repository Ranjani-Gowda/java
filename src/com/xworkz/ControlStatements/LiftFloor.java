package com.xworkz.ControlStatements;

public class LiftFloor {

	public static void main(String[] args) {
    int lift = 8;
    switch(lift) {
    case 1: System.out.println("1st Floor");
    break;
    case 2: System.out.println("2nd Floor");
    break;
    case 3: System.out.println("3rd Floor");
    break;
    case 4: System.out.println("4th Floor");
    break;
    case 5: System.out.println("5th Floor");
    break;
    case 6: System.out.println("6th Floor");
    break;
    case 7: System.out.println("7th Floor");
    break;
    default : System.out.println("Invalid Floor");
    }
	}

}
